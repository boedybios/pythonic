a = [10, 20, 30]
b = [40, 50, 60]
###################################################
# CONVENTIONAL WAY
###################################################
c = [None] * (len(a) + len(b))
for i in range(len(a)):
    c[i] = a[i]
for i in range(len(b)):
    c[i+len(a)] = b[i]
print(c)
###################################################
# PYTHONIC WAY
###################################################
c = a + b
print(c)
